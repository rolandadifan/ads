{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $message = Message::orderBy('created_at', 'desc')->get();
        return view('Admin.pages.message')->with([
            'message' => $message
        ]);
    }

